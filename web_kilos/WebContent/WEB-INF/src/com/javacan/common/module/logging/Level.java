package com.javacan.common.module.logging;

public class Level {
    public static Level DEBUG = new Level("DEBUG", 0);
    public static Level INFO = new Level("INFO", 3);
    public static Level WARN = new Level("WARN", 6);
    public static Level ERROR = new Level("ERROR", 9);
    public static Level FATAL = new Level("FATAL", 12);
    
    private int value;
    private String name;
    
    public Level(String name, int value) {
        this.value = value;
        this.name = name;
    }
    
    public int getValue() {
        return value;
    }
    
    public String getName() {
        return name;
    }
}
